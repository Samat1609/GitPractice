package Switch1;

public class Switch1 {
    public static void main(String[] args) {

        String seasons = "Winter";

        switch (seasons) {
            case "Winter" :
                System.out.println("kfkd");
                break;
            case "Summer":
                System.out.println("gfgfgf");
                break;
            default:
                System.out.println("fffffffffff");


        }
    }
}
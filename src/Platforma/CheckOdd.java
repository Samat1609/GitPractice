package Platforma;

public class CheckOdd {
    public static void main(String[] args) {
        int number=3;
        System.out.println("The number is :"+number);
        if (number % 2==0){
            System.out.println("Это четное число ");
        }else {
            System.out.println("Это нечетное число");
        }
        System.out.println("Bye");
    }
}

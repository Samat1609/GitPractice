package Platforma;

import java.util.Scanner;

public class Cub {
    public static void main(String[] args) {
        Scanner scanner=new Scanner(System.in);
        int num= scanner.nextInt();
        int num2= scanner.nextInt();
        int num3= scanner.nextInt();
        int num4= scanner.nextInt();

        System.out.println("Куб первого значение "+num*num*num);
        System.out.println("Куб второго значение "+num2*num2*num2);
        System.out.println("Куб третего значение "+num3*num3*num3);
        System.out.println("Куб четвертого значение "+num4*num4*num4);



    }
}

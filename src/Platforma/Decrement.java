package Platforma;

public class Decrement {
    public static void main(String[] args) {
        int x=1;
        System.out.println(x);  //1
        System.out.println(++x);  //2
        System.out.println(x++);  //2
        System.out.println(--x);  // 2
        System.out.println(x--);  // 2
        System.out.println(x);  //1
    }
}
